package com.nickshock.task7.presentationLayer;

/**
 * View class that prints gotten information to console.
 *
 * @author Barysevich Nikalai
 * @version 1.2 16 June 2018
 */
public class ConsolePrinter {

    /**
     * Prints gotten byte to console.
     *
     * @param info info to be printed.
     */
    public static void print(byte info) {
        System.out.println(info);
    }

    /**
     * Prints gotten short to console.
     *
     * @param info info to be printed.
     */
    public static void print(short info) {
        System.out.println(info);
    }

    /**
     * Prints gotten char to console.
     *
     * @param info info to be printed.
     */
    public static void print(char info) {
        System.out.println(info);
    }

    /**
     * Prints gotten int to console.
     *
     * @param info info to be printed.
     */
    public static void print(int info) {
        System.out.println(info);
    }

    /**
     * Prints gotten long to console.
     *
     * @param info info to be printed.
     */
    public static void print(long info) {
        System.out.println(info);
    }

    /**
     * Prints gotten float to console.
     *
     * @param info info to be printed.
     */
    public static void print(float info) {
        System.out.println(info);
    }

    /**
     * Prints gotten double to console.
     *
     * @param info info to be printed.
     */
    public static void print(double info) {
        System.out.println(info);
    }

    /**
     * Prints gotten boolean to console.
     *
     * @param info info to be printed.
     */
    public static void print(boolean info) {
        System.out.println(info);
    }

    /**
     * Prints gotten object to console.
     *
     * @param info info to be printed.
     */
    public static void print(Object info) {
        System.out.println(info);
    }
}
